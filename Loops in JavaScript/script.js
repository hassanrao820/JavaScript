// Loops in JavaScript are:-

// For loop
for(let a = 1; a <= 5; a = a + 1){
    console.log('Hassan' + a);
}

// While loop
let initialPosition = 1;
let yourHome = 10;
while(initialPosition != yourHome){
    initialPosition = initialPosition + 1;
    console.log('Step Taken' + initialPosition);
}

// Do while loop
let yourPosition = 1;
let yourHouse = 15;
do{
    yourPosition = yourPosition + 1;
    console.log('Almost Reach House' + yourPosition);
}while(yourPosition <= yourHouse);